foreach ($dois as $doi)
{
	$url = 'https://journalmap.org//api/articles.json'
		. '?key=' . $config['journal_map_key']
		. '&version=1.0'
		. '&query=' . $doi ;
		
	echo $url . "\n";
	
	$json = get($url);

	echo $json;

	$results = json_decode($json);

	print_r($results);

	foreach ($results as $result)
	{
		// sanity check
		$ok = false;
		if (isset($result->doi))
		{
			$ok = ($result->doi == $doi);
		}
		
		if ($ok)
		{
			$result->_id = 'articles/' . $result->id;
		
			echo $result->_id . "\n";
	
			$couch->add_update_or_delete_document($result,  $result->_id);
		}	
	}
}


